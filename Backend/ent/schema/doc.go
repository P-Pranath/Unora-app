//   go run entgo.io/ent/cmd/ent new <SchemaName>
//
// To generate code from schemas:
//   go run entgo.io/ent/cmd/ent generate ./ent/schema --target ./ent/generated
//
// Or use Makefile:
//   make ent-init    # Create new schema
//   make ent-generate # Generate code
